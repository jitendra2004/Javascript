// primitive datatypes
// Number
let age=10;
console.log(age);

// boolean 
let It_is_raining=true;
console.log(It_is_raining);

// string
let address="shri swami samarath chawl manorama nagar thane(400607)";
console.log(address);

// Null
let balance=null;
console.log(balance);

// Undefined
let name;
console.log(name);

// Non primitive datatypes
// Array
let arr=[10,20,"jitendra",null,10];
console.log(arr);

// object 
let obj={
    name:"jitendra",
    age:"20",
    study:true
};
console.log(obj);

