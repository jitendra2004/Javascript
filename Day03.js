// Type conversion
// String to number
let str="10";
let str1="name";
console.log(Number(str));
console.log(Number(str1));  // Invalid string to number

// boolean to number
let bool=true;
let bool1=false;
console.log(Number(bool));
console.log(Number(bool1));

// Null to Number
let balance=null;
console.log(Number(balance));

// Undefined to number 
let age;
console.log(Number(age));

// Number to string 
let num=10;
console.log(String(num));

// boolean to string 
let boo=true;
console.log(String(boo));

// string to booelean
let strr="my name is jitendra";
